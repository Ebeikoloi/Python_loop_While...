#Flow control statements
#if_statement syntax

'''
if condition/expression(True/False):
    block of code - line 1
    block of code - line 2
else condition/expression (True/False):
    block of code 
else:
    block of code
'''



#Nested if statement
'''
if condition/expression(True/False):
    block of code - line 1
    block of code - line 2
    if condition:
        block of code
    else:
        block of code
else condition/expression (True/False):
    block of code 
else:
    block of code
'''


#Example
x=13
